<h2><b>Criminal Investigation Tracker</b></h2>
<i>
<h2>We here propose a criminal investigation tracker system that tracks the investigation 
status of criminal cases with logs and also predicts primary suspects. The system is 
proposed to help agencies like CBI, CID and other such bureauâ€™s to speed up 
investigation process and track status of multiple cases at a time. The system keeps 
logs of a case which includes case summary, people involved, disputes, past 
criminal history of those involved, Items recovered on scene and other details. The 
system realizes the type of case, allows admin to update the status of investigation, 
upload more images of crime, items found on scene etc. This allows authorized 
officers to check case status and look into its status online and also update any 
important info as and when needed. The system also consists of a suspect 
prediction algorithm. Based on type of case, property, land, love or other entities 
involved the system studies past cases, it studies past criminal records of those 
involved and based on this data it provides suggestions of suspected persons in a 
logical order. The system is designed to aid investigation teams to work collectively 
on cases, coordinate and also speed up the process by suggesting logical suspects 
based on data provided.</h2>
